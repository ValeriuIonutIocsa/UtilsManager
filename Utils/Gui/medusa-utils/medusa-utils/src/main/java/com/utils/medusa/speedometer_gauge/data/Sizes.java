package com.utils.medusa.speedometer_gauge.data;

public class Sizes {

	private final double width;
	private final double height;

	public Sizes(
			final double width,
			final double height) {

		this.width = width;
		this.height = height;
	}

	public double getWidth() {
		return width;
	}

	public double getHeight() {
		return height;
	}
}
